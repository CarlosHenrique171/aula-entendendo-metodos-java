package calculadora.model;

public class Operacoes {

	
	public static double somaDoisNumeros(double valorA,double valorB) {
		return valorA + valorB;
	}
	
	public static double subtracaoDoisNumeros(double valorA,double valorB) {
		return valorA - valorB;
	}
	
	public static double divisaoDoisNumeros(double valorA,double valorB) {
		return valorA / valorB;
	}
	
	public static double multiplicacaoDoisNumeros(double valorA,double valorB) {
		return valorA * valorB;
	}

}
